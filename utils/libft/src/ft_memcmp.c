#include <libft.h>

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	const unsigned char	*s1p;
	const unsigned char	*s2p;

	if (!s1 && !s2)
		return (0);
	s1p = (const unsigned char *)s1;
	s2p = (const unsigned char *)s2;
	while (n--)
	{
		if (*s1p != *s2p)
			return (*s1p - *s2p);
		s1p++;
		s2p++;
	}
	return (0);
}
